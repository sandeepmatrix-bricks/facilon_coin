            <h2 class="mt-4">Our Business Activity</h2>

            <p>Facilon is a facilitator. Facilon is not a Financial / Investment / Wealth Adviser nor a Financial / Investment / Insurance Product distributor.</p>

            <p>Facilon offers a comprehensive platform designed to streamline operations for Service Providers viz Brokers, Custodians, Banks, Portfolio Managers etc., while enhancing the experience for their clients, who are investors or their investment advisors.</p>

            <ul style="list-style-type: disc; padding-left: 20px;">
              <li><b style="color: #be1717;">For Service Providers</b>, Facilon offers solution for facilitating client access to Service Provider products and efficient onboarding of these clients.</li>
              <li><b style="color: #be1717;">For Investors and their Investment Advisors </b>(including Wealth Managers, External Asset Managers - collectively referred as ‘Service Recipients’ or ‘SR’), Facilon provides solutions for facilitating interaction with these SPs through their pre-onboarding, onboarding and post onboarding investment journey.</li>
            </ul>

            <p>Facilon is a market place where the documents and information flow is intended to achieve the certain key objectives to Service Recipients and Service Providers.</p>

            <p>Facilon acts as a bridge or a facilitator to electronically facilitate the flow of information (in the form of data, documents, comments, observations etc) between onshore parties such as custodians, designated depository participants (DDPs), depository participants, brokers, banks, portfolio managers, investment funds and identified tax consultant on one side (“Service Provider”/ “SP”) and onshore/offshore (foreign parties) such as investors (institutions, body corporate or individuals such as NRIs, OCIs or foreign resident individuals), investment advisors and professional firms, who can source investors on other side (“Service Recipient”/“SR”).</p>
